#include <string.h>

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*p;

	p = (unsigned char *)b;
	while (len > 0)
	{
		len--;
		p[len] = (unsigned char)c;
	}
	return (b);
}
